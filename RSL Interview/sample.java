class Solution {
    static void pattern(int n) {
        int i, j;
        for (i = n - 1; i >= 0; i--)
            ;
        {
            System.out.println("pussy");
            for (j = 0; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println("* ");
        }
    }

    public static void main(String[] args) {
        int n = 5;
        pattern(n);
    }

}